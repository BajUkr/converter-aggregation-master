namespace ConverterDictionaryAggregation
{
    /// <summary>
    /// An enumeration consists of a set of words for all characters that a real number can contains.
    /// </summary>
    public enum Character : byte
    {
        /// <summary>
        /// Represents the digit 0.
        /// </summary>
        Zero,

        /// <summary>
        /// Represents the digit 1.
        /// </summary>
        One,

        /// <summary>
        /// Represents the digit 2.
        /// </summary>
        Two,

        /// <summary>
        /// Represents the digit 3.
        /// </summary>
        Three,

        /// <summary>
        /// Represents the digit 4.
        /// </summary>
        Four,

        /// <summary>
        /// Represents the digit 5.
        /// </summary>
        Five,

        /// <summary>
        /// Represents the digit 6.
        /// </summary>
        Six,

        /// <summary>
        /// Represents the digit 7.
        /// </summary>
        Seven,

        /// <summary>
        /// Represents the digit 8.
        /// </summary>
        Eight,

        /// <summary>
        /// Represents the digit 9.
        /// </summary>
        Nine,

        /// <summary>
        /// Represents the plus sign (+).
        /// </summary>
        Plus,

        /// <summary>
        /// Represents the minus sign (-).
        /// </summary>
        Minus,

        /// <summary>
        /// Represents the decimal point (.).
        /// </summary>
        Point,

        /// <summary>
        /// Represents the decimal comma (,).
        /// </summary>
        Comma,

        /// <summary>
        /// Represents the exponent symbol (E or e).
        /// </summary>
        Exponent,

        /// <summary>
        /// Represents the Not-a-Number (NaN) value.
        /// </summary>
        NaN,

        /// <summary>
        /// Represents the positive infinity value.
        /// </summary>
        PositiveInfinity,

        /// <summary>
        /// Represents the negative infinity value.
        /// </summary>
        NegativeInfinity,

        /// <summary>
        /// Represents the smallest positive double value greater than zero.
        /// </summary>
        Epsilon,
    }
}
